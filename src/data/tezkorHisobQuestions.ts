export type TezkorHisobDifficulty = 'Oson' | "O'rta" | 'Qiyin'

export type TezkorHisobChoiceTask = {
  id: string
  type: 'choice'
  prompt: string
  options: string[]
  correctIndex: number
  topic: '+' | '-' | '*' | '/'
}

const makeChoice = (
  id: string,
  prompt: string,
  options: [string, string, string, string],
  answer: string,
  topic: '+' | '-' | '*' | '/',
): TezkorHisobChoiceTask => {
  const correctIndex = options.indexOf(answer)
  if (correctIndex < 0) {
    throw new Error(`Tezkor hisob savolida javob topilmadi: ${id}`)
  }
  return {
    id,
    type: 'choice',
    prompt,
    options: [...options],
    correctIndex,
    topic,
  }
}

